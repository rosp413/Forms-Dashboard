    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Connection con;
        try {
            Class.forName("net.ucanaccess.jdbc.UcanaccessDriver");
            File accessFile = new File("src/main/java/com/raven/database/loginpage.accdb");
            String jdbcUrl = "jdbc:ucanaccess://" + accessFile.getAbsolutePath();
            con = DriverManager.getConnection(jdbcUrl);
            System.out.println("Connection Established");
            PreparedStatement ps = con.prepareStatement("Insert into user_pass(username,email,password) values(?,?,?)");
            ps.setString(1,username1.getText());   
            ps.setString(2,email.getText());
            ps.setString(3,password1.getText());
            if(username1.getText().trim().isEmpty() && email.getText().trim().isEmpty() && password1.getText().trim().isEmpty() ){
                JOptionPane.showMessageDialog(null,"Enter the details first");
            }
            else {
                JOptionPane.showMessageDialog(null,"Your Account is created successfully! Please login");
            }
            System.out.println("Got Data");
            ps.executeUpdate();
        }
        catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

